// Helper function to get all topics
export function getAllTopics(): string[] {
  return blogTopics.flatMap(category => category.topics);
}

// Helper function to get topics by category
export function getTopicsByCategory(categoryName: string): string[] {
  const category = blogTopics.find(cat => cat.category === categoryName);
  return category ? category.topics : [];
}

// Helper function to get random topic
export function getRandomTopic(): { topic: string; category: string; icon: string } {
  const categoryIndex = Math.floor(Math.random() * blogTopics.length);
  const category = blogTopics[categoryIndex];
  const topicIndex = Math.floor(Math.random() * category.topics.length);
  
  return {
    topic: category.topics[topicIndex],
    category: category.category,
    icon: category.icon,
  };
}

// Track used topics to avoid duplicates
const usedTopics = new Set<string>();

// Get unique random topic
export function getUniqueRandomTopic(): { topic: string; category: string; icon: string } | null {
  const allTopics = getAllTopics();
  const availableTopics = allTopics.filter(topic => !usedTopics.has(topic));
  
  if (availableTopics.length === 0) {
    // Reset if all topics have been used
    usedTopics.clear();
    return getRandomTopic();
  }
  
  // Find a topic that hasn't been used
  let selected = getRandomTopic();
  while (usedTopics.has(selected.topic)) {
    selected = getRandomTopic();
  }
  
  usedTopics.add(selected.topic);
  return selected;
}

// Get topics for specific time of day
export function getTopicForTimeOfDay(hour: number): { topic: string; category: string; icon: string } {
  // Morning (2 AM, 6 AM): Educational guides
  if (hour >= 2 && hour < 10) {
    const educationalCategories = ['SPECIALTY_GUIDES', 'USMLE_STRATEGY', 'RESEARCH_GUIDES', 'FINANCIAL_PLANNING'];
    const category = educationalCategories[Math.floor(Math.random() * educationalCategories.length)];
    const topics = getTopicsByCategory(category);
    const topic = topics[Math.floor(Math.random() * topics.length)];
    const cat = blogTopics.find(c => c.category === category)!;
    return { topic, category, icon: cat.icon };
  }
  
  // Midday (10 AM, 2 PM): Application tips
  if (hour >= 10 && hour < 18) {
    const applicationCategories = ['APPLICATION_TIPS', 'PERSONAL_STATEMENT', 'PROGRAM_SELECTION', 'TIMELINE_PLANNING'];
    const category = applicationCategories[Math.floor(Math.random() * applicationCategories.length)];
    const topics = getTopicsByCategory(category);
    const topic = topics[Math.floor(Math.random() * topics.length)];
    const cat = blogTopics.find(c => c.category === category)!;
    return { topic, category, icon: cat.icon };
  }
  
  // Evening (6 PM, 10 PM): Success stories & interviews
  const eveningCategories = ['SUCCESS_STORIES', 'INTERVIEW_PREP', 'MATCH_STRATEGY', 'WELLNESS_BALANCE'];
  const category = eveningCategories[Math.floor(Math.random() * eveningCategories.length)];
  const topics = getTopicsByCategory(category);
  const topic = topics[Math.floor(Math.random() * topics.length)];
  const cat = blogTopics.find(c => c.category === category)!;
  return { topic, category, icon: cat.icon };
}